// Convert temperature anomaly to chart coordinates (0-100 scale)
export function temperatureToChartY(tempAnomaly: number): number {
  // Map temperature range (-0.6Â°C to +2.0Â°C) to chart height (100 to 0)
  const minTemp = -0.6;
  const maxTemp = 2.0;
  const normalizedTemp = (tempAnomaly - minTemp) / (maxTemp - minTemp);
  return 100 - (normalizedTemp * 100);
}

// Get temperature data for a specific year range
export function getTemperatureDataForRange(startYear: number, endYear: number) {
  return HISTORICAL_TEMPERATURE_DATA.filter(
    data => data.year >= startYear && data.year <= endYear
  );
}